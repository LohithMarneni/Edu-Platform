    if (!selectedSubject || !selectedLesson) return [];
    
    const lesson = collections[selectedSubject].lessons[selectedLesson];
    if (!lesson) return [];

    if (activeTab === 'custom-folders') {
      return Object.entries(lesson.content['custom-folders'] || {}).map(([id, folder]) => ({
        id,
        ...folder,
        type: 'folder',
      }));
    }

    return lesson.content[activeTab] || [];
