    const updatedCollections = { ...collections };
    if (!updatedCollections[selectedSubject].lessons[selectedLesson].content['custom-folders']) {
      updatedCollections[selectedSubject].lessons[selectedLesson].content['custom-folders'] = {};
    }

    const folderId = newFolderName.toLowerCase().replace(/\s+/g, '-');
    updatedCollections[selectedSubject].lessons[selectedLesson].content['custom-folders'][folderId] = {
      name: newFolderName,
      items: [],
    };

