    if (!collections[subject]?.lessons[lesson]?.content[type]) return 0;
    if (type === 'custom-folders') {
      return Object.keys(collections[subject].lessons[lesson].content[type]).length;
    }
    return collections[subject].lessons[lesson].content[type].length;
